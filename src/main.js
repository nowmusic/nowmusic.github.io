const router = () => {
    const path = location.hash.substr(1);
    const app = document.querySelector('.app');
    const route = [{ '': Main },
        {start: First } ,
        {good : Second},
        {normal : Third},
        {bad : Fourth},
        {good_happy : Happy},
        {good_excited : Excited},
        {good_pitapat : Pitapat},
        {good_pleased : Pleased},
        {good_proud : Proud},
        {good_relaxed : Relaxed},
        {normal_soso : SoSo},
        {normal_sleep : Sleep},
        {normal_miss : Miss},
        {normal_upndown : Upndown},
        {normal_lonely : Lonely},
        {normal_wantsleep : Wantsleep},
        {normal_busy : Busy},
        {bad_bummed : Bummed},
        {bad_sad : Sad},
        {bad_cry : Cry},
        {bad_hard : Hard},
        {bad_disappointed : Disappointed},
        {bad_worried : Worried},
        {bad_annoying : Annoying},
        {bad_breakdown : Breakdown},
        {bad_crazy : Crazy},
        {bad_bad : Bad},
        {bad_tired : Tired},
        {bad_crooked : Crooked},
        {bad_comforted : Comforted}
];

    const target = route.filter(e => Object.keys(e)[0] === path);

    app.innerHTML = Object.values(target[0])[0]();
};

window.onload = function () {
    router();
};
